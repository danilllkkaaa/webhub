const dictionaries: Record<Exclude<Language, 'ru'>, Record<string, string>> = { en, kk }

function preserveWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] ?? ''
  const trailing = source.match(/\s*$/)?.[0] ?? ''
  return `${leading}${translated}${trailing}`
}

export function translateText(value: string, language: Language): string {
  if (language === 'ru' || !/[А-Яа-яЁё]/.test(value)) return value
  const dictionary = dictionaries[language]
  const exact = new Map(Object.entries(dictionary))
  const orderedEntries = Object.entries(dictionary).sort((a, b) => b[0].length - a[0].length)

  const trimmed = value.trim()
  if (!trimmed) return value

  const exactTranslation = exact.get(trimmed)
  if (exactTranslation) return preserveWhitespace(value, exactTranslation)

  let translated = value
  for (const [ru, translation] of orderedEntries) {
    if (translated.includes(ru)) translated = translated.split(ru).join(translation)
  }

  translated = translated
  translated = applyLanguagePatterns(translated, language)

  return translated
}

function applyLanguagePatterns(value: string, language: Exclude<Language, 'ru'>) {
  if (language === 'kk') {
    return value
      .replace(/(\d+)\s*ур\./g, '$1 сабақ')
      .replace(/(\d+)\s*студ\./g, '$1 студент')
      .replace(/(\d+)\s*мод\./g, '$1 модуль')
      .replace(/(\d+)\s*учеников/g, '$1 оқушы')
      .replace(/(\d+)\s*уроков/g, '$1 сабақ')
      .replace(/(\d+)\s*регистрация/g, '$1 тіркелу')
      .replace(/(\d+)\s*место/g, '$1 орын')
      .replace(/(\d+)\s*уровень/g, '$1 деңгей')
      .replace(/Все\s+(\d+)\s+курса/g, 'Барлық $1 курс')
      .replace(/Ничего не найдено по запросу «(.+)»/g, '"$1" бойынша ештеңе табылмады')
      .replace(/Ваше имя:\s*/g, 'Атыңыз: ')
      .replace(/Ваша почта:\s*/g, 'Email: ')
      .replace(/Удалить доступ для\s*/g, 'Қолжетімділікті жою: ')
      .replace(/Открыть\s+/g, 'Ашу ')
      .replace(/Модуль\s+(\d+)/g, 'Модуль $1')
      .replace(/Урок\s+(\d+)/g, 'Сабақ $1')
  }

  return value
    .replace(/(\d+)\s*ур\./g, '$1 lessons')
    .replace(/(\d+)\s*студ\./g, '$1 students')
    .replace(/(\d+)\s*мод\./g, '$1 modules')
    .replace(/(\d+)\s*учеников/g, '$1 students')
    .replace(/(\d+)\s*уроков/g, '$1 lessons')
    .replace(/(\d+)\s*регистрация/g, '$1 registration')
    .replace(/(\d+)\s*место/g, '$1 seat')
    .replace(/(\d+)\s*уровень/g, 'level $1')
    .replace(/Все\s+(\d+)\s+курса/g, 'All $1 courses')
    .replace(/Ничего не найдено по запросу «(.+)»/g, 'Nothing found for "$1"')
    .replace(/Ваше имя:\s*/g, 'Your name: ')
    .replace(/Ваша почта:\s*/g, 'Your email: ')
    .replace(/Удалить доступ для\s*/g, 'Remove access for ')
    .replace(/Открыть\s+/g, 'Open ')
    .replace(/Модуль\s+(\d+)/g, 'Module $1')
    .replace(/Урок\s+(\d+)/g, 'Lesson $1')
}
